class CNN(nn.Module):
    def __init__(self, num_classes):
        super().__init__()
        self.features = nn.Sequential(
            nn.Conv2d(3,16,3,2), nn.BatchNorm2d(16), nn.LeakyReLU(),
            nn.MaxPool2d(2,2),
            nn.Conv2d(16,32,3,2), nn.BatchNorm2d(32), nn.LeakyReLU(),
            nn.MaxPool2d(2,2),
            nn.Conv2d(32,64,3,2), nn.BatchNorm2d(64), nn.LeakyReLU(),
            nn.MaxPool2d(2,2)
        )
        self.classifier = nn.Sequential(
            nn.Dropout(0.2),
            nn.Linear(64*3*3,128), nn.ReLU(),
            nn.Dropout(0.2),
            nn.Linear(128,num_classes)
        )
    def forward(self,x):
        x = self.features(x)
        x = x.view(x.size(0), -1)
        x = self.classifier(x)
        return x

device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
model = CNN(NUM_CLASSES).to(device)
criterion = nn.CrossEntropyLoss()
optimizer = torch.optim.Adam(model.parameters(), lr=1e-3)

