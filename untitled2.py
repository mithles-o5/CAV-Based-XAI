class ProductionCAV:
    """Production-grade CAV implementation from your friend's code"""

    def __init__(self, concept_name="", use_ensemble=True):
        self.concept_name = concept_name
        self.use_ensemble = use_ensemble
        self.scaler = StandardScaler()

        self.classifiers = {
            'logistic': LogisticRegression(max_iter=2000, random_state=42),
            'svm': SVC(probability=True, random_state=42, kernel='rbf'),
            'rf': RandomForestClassifier(n_estimators=100, random_state=42)
        }

        self.trained_classifiers = {}
        self.concept_vector = None
        self.training_accuracy = 0.0
        self.cross_val_scores = {}
        self.is_trained = False

    def train(self, concept_activations, random_activations, validation_split=0.2):
        if len(concept_activations) == 0 or len(random_activations) == 0:
            print(f"⚠️ Warning: Empty activations for {self.concept_name}")
            return 0.0

        X = np.concatenate([concept_activations, random_activations])
        y = np.concatenate([
            np.ones(len(concept_activations)),
            np.zeros(len(random_activations))
        ])

        X_scaled = self.scaler.fit_transform(X)

        print(f"  Training CAV for {self.concept_name}...")
        best_accuracy = 0.0
        best_classifier_name = None

        for name, classifier in self.classifiers.items():
            try:
                classifier.fit(X_scaled, y)
                cv_scores = cross_val_score(classifier, X_scaled, y, cv=3)
                self.cross_val_scores[name] = cv_scores
                train_acc = classifier.score(X_scaled, y)

                print(f"      {name}: Train={train_acc:.3f}, CV={cv_scores.mean():.3f}±{cv_scores.std():.3f}")

                if train_acc > best_accuracy:
                    best_accuracy = train_acc
                    best_classifier_name = name

                self.trained_classifiers[name] = classifier

            except Exception as e:
                print(f"      Error training {name}: {e}")
                continue

        if best_classifier_name and hasattr(self.trained_classifiers[best_classifier_name], 'coef_'):
            self.concept_vector = self.trained_classifiers[best_classifier_name].coef_[0]
            self.concept_vector = self.concept_vector / (np.linalg.norm(self.concept_vector) + 1e-8)
        elif best_classifier_name == 'rf':
            self.concept_vector = self.trained_classifiers['rf'].feature_importances_
            self.concept_vector = self.concept_vector / (np.linalg.norm(self.concept_vector) + 1e-8)

        self.training_accuracy = best_accuracy
        self.is_trained = True

        print(f"    ✅ Best: {best_classifier_name} with {best_accuracy:.3f} accuracy")
        return best_accuracy

    def get_score(self, activations, method='ensemble'):
        if not self.is_trained:
            return np.array([0.5])

        try:
            activations_scaled = self.scaler.transform(activations)
        except Exception as e:
            print(f"Scaling error: {e}")
            return np.array([0.5])

        if method == 'ensemble' and len(self.trained_classifiers) > 1:
            predictions = []
            for name, classifier in self.trained_classifiers.items():
                try:
                    pred = classifier.predict_proba(activations_scaled)[:, 1]
                    predictions.append(pred)
                except Exception as e:
                    print(f"Prediction error for {name}: {e}")
                    continue

            if predictions:
                return np.mean(predictions, axis=0)
            else:
                return np.array([0.5])
        else:
            return np.array([0.5])

def get_robust_activations(model, input_tensor, layer):
    """Robust activation extraction"""
    activations = {'value': None}

    def hook_fn(module, input, output):
        if output is not None:
            activations['value'] = output.detach().cpu().clone()

    hook = layer.register_forward_hook(hook_fn)

    try:
        with torch.no_grad():
            model.eval()
            _ = model(input_tensor)
    except Exception as e:
        print(f"Forward pass error: {e}")
        return None
    finally:
        hook.remove()
        torch.cuda.empty_cache()

    return activations['value']

def collect_high_quality_activations(image_paths, model, layer, transform, max_samples=50):
    """Collect activations for CAV training"""
    activations = []
    torch.cuda.empty_cache()

    pbar = tqdm(image_paths[:max_samples], desc="Collecting activations")

    for img_path in pbar:
        try:
            image = cv2.imread(img_path)
            if image is None:
                continue

            image = cv2.cvtColor(image, cv2.COLOR_BGR2RGB)
            transformed = transform(image=image)["image"]
            input_tensor = transformed.unsqueeze(0).to(DEVICE)

            act = get_robust_activations(model, input_tensor, layer)
            if act is not None and act.numel() > 0:
                act_flat = act.view(act.size(0), -1).numpy()
                activations.append(act_flat)

            del input_tensor, act

            if len(activations) % 10 == 0:
                torch.cuda.empty_cache()
                gc.collect()

        except Exception as e:
            continue

    if len(activations) == 0:
        print("⚠️ Warning: No activations collected!")
        return np.array([]).reshape(0, -1)

    result = np.concatenate(activations, axis=0)
    print(f"✅ Collected {result.shape[0]} activations of dimension {result.shape[1]}")
    return result

