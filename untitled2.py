def save_checkpoint(model, optimizer, epoch, batch_idx, best_acc, path):
    state = {
        'epoch': epoch,
        'batch_idx': batch_idx,
        'best_acc': best_acc,
        'model_state_dict': model.state_dict(),
        'optimizer_state_dict': optimizer.state_dict(),
        'scaler_state_dict': scaler.state_dict() if scaler is not None else None,
    }
    os.makedirs(os.path.dirname(path), exist_ok=True)
    torch.save(state, path)
    print(f"Checkpoint saved to {path} at Epoch {epoch+1}, Batch {batch_idx}")

def load_checkpoint(model, optimizer, path):
    if not os.path.exists(path):
        print("No checkpoint found. Starting training from scratch.")
        return 0, 0, 0.0

    print(f"Loading checkpoint from {path}...")
    try:
        checkpoint = torch.load(path, map_location=DEVICE)
        model.load_state_dict(checkpoint['model_state_dict'])
        optimizer.load_state_dict(checkpoint['optimizer_state_dict'])

        if scaler is not None and 'scaler_state_dict' in checkpoint and checkpoint['scaler_state_dict'] is not None:
            scaler.load_state_dict(checkpoint['scaler_state_dict'])

        start_epoch = int(checkpoint.get('epoch', 0))
        start_batch_idx = int(checkpoint.get('batch_idx', 0))
        best_acc = float(checkpoint.get('best_acc', 0.0))

        print(f"Resumed from Epoch {start_epoch}, Batch {start_batch_idx}. Best accuracy so far: {best_acc:.2f}%")
        return start_epoch, start_batch_idx, best_acc
    except Exception as e:
        print(f"Error loading checkpoint from {path}: {e}. Starting from scratch.")
        return 0, 0, 0.0

