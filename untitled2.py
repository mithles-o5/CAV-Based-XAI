def get_robust_activations(model, input_tensor, layer):
    """Robust activation extraction with error handling"""
    activations = {'value': None}

    def hook_fn(module, input, output):
        if output is not None:
            # Store on CPU to save GPU memory
            activations['value'] = output.detach().cpu().clone()

    hook = layer.register_forward_hook(hook_fn)

    try:
        with torch.no_grad():
            model.eval()
            _ = model(input_tensor)
    except Exception as e:
        print(f"Forward pass error: {e}")
        return None
    finally:
        hook.remove()
        torch.cuda.empty_cache()

    return activations['value']

def collect_high_quality_activations(image_paths, model, layer, transform, max_samples=50):
    """Collect high-quality activations with simplified approach"""
    activations = []
    torch.cuda.empty_cache()

    pbar = tqdm(image_paths[:max_samples], desc="Collecting activations")

    for img_path in pbar:
        try:
            # Load and preprocess image
            image = cv2.imread(img_path)
            if image is None:
                continue

            image = cv2.cvtColor(image, cv2.COLOR_BGR2RGB)
            transformed = transform(image=image)["image"]
            input_tensor = transformed.unsqueeze(0).to(DEVICE)

            # Get activations
            act = get_robust_activations(model, input_tensor, layer)
            if act is not None and act.numel() > 0:
                act_flat = act.view(act.size(0), -1).numpy()
                activations.append(act_flat)

            del input_tensor, act

            # Memory cleanup every 10 images
            if len(activations) % 10 == 0:
                torch.cuda.empty_cache()
                gc.collect()

        except Exception as e:
            continue

    if len(activations) == 0:
        print("⚠️ Warning: No activations collected!")
        return np.array([]).reshape(0, -1)

    result = np.concatenate(activations, axis=0)
    print(f"✅ Collected {result.shape[0]} activations of dimension {result.shape[1]}")
    return result

